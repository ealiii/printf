#include "ft_printf.h"

int	printc(int c)
{
	write(1, &c, 1);
	return (1);
}
//////////////////////////////////////////////////////////////////////////

int	prints(char *s)
{
	int	i;

	i = 0;
	if (s == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (s[i] != '\0')
	{
		printc(s[i]);
		i++;
	}
	return (i);
}
//////////////////////////////////////////////////////////////////////////

int	printdi(int d)
{
	int	c;

	c = 0;
	if (d == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	else if (d < 0)
	{
		c += write(1, "-", 1);
		d = -d;
		c += printdi(d);
	}
	else if (d > 9)
	{
		c += printdi(d / 10);
		c += printdi(d % 10);
	}
	else
	{
		c += printc(d + 48);
	}
	return (c);
}

//////////////////////////////////////////////////////////////////////////

int	printu(unsigned int d)
{
	int	c;

	c = 0;
	if (d > 9)
	{
		c += printu(d / 10);
		c += printu(d % 10);
	}
	else
		c += printc(d + '0');
	return (c);
}

